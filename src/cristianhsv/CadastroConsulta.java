    public void atualizarTabela() {
        DefaultTableModel tabela = (DefaultTableModel) viewConsultas.getModel();
        tabela.setRowCount(0);

        ArrayList lista = consultaDAO.listar();
        Iterator it = lista.iterator();
        while (it.hasNext()) {
            ConsultaDaTabela consulta = (ConsultaDaTabela) it.next();
            tabela.addRow(new Object[]{consulta.getId(), consulta.getMedico(), consulta.getPaciente(), consulta.getConvenio()});
        }
    }

    public void limpar() {
        medicoCB.removeAllItems();
        ArrayList arrayMed = medicoDAO.listar();
        for (int counter = 0; counter < arrayMed.size(); counter++) {
            Medico medico = (Medico) arrayMed.get(counter);
            medicoCB.addItem(Integer.toString(medico.getId()) + " - " + medico.getNome());
        }
        pacienteCB.removeAllItems();
        ArrayList arrayPac = pacienteDAO.listar();
        for (int counter = 0; counter < arrayPac.size(); counter++) {
            Paciente paciente = (Paciente) arrayPac.get(counter);
            pacienteCB.addItem(Integer.toString(paciente.getId()) + " - " + paciente.getNome());
        }
        txtId.setText("");
        convenioTxt.setText("");
        btnExcluir.setEnabled(false);
    }

