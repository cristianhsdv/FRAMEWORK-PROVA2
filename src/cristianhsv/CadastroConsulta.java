    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
        // TODO add your handling code here:
        if (convenioTxt.getText().trim().isEmpty() || medicoCB.getSelectedItem() == null || pacienteCB.getSelectedItem() == null) {
            JOptionPane.showMessageDialog(rootPane, "PREENCHA TODOS OS CAMPOS");
        } else {
            Consulta con = new Consulta();
            con.setConvenio(convenioTxt.getText().trim());

            String item = (String) medicoCB.getSelectedItem();
            con.setIdMedico(Integer.parseInt(item.split(" - ")[0]));
            String pacienteItem = (String) pacienteCB.getSelectedItem();
            con.setIdPaciente(Integer.parseInt(pacienteItem.split(" - ")[0]));

            String id = txtId.getText().trim();
            String msg;
            if (id.isEmpty()) {
                msg = consultaDAO.cadastrar(con);
                if (msg.equals("1")) {
                    msg = "CADASTRO EFETUADO COM SUCESSO";
                }

                JOptionPane.showMessageDialog(rootPane, msg);
            } else {
                con.setId(Integer.parseInt(id));
                msg = consultaDAO.atualizar(con);
                if (msg.equals("1")) {
                    msg = "ALTERAÇÃO EFETUADA COM SUCESSO";
                }

                JOptionPane.showMessageDialog(rootPane, msg);
            }

            this.limpar();
            this.atualizarTabela();
        }
    }//GEN-LAST:event_btnSalvarActionPerformed

    private void viewConsultasMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_viewConsultasMouseClicked
        // TODO add your handling code here:
        String id = viewConsultas.getValueAt(viewConsultas.getSelectedRow(), 0).toString();
        String medicoLabel = viewConsultas.getValueAt(viewConsultas.getSelectedRow(), 1).toString();
        String pacienteLabel = viewConsultas.getValueAt(viewConsultas.getSelectedRow(), 2).toString();
        String convenio = viewConsultas.getValueAt(viewConsultas.getSelectedRow(), 3).toString();

        txtId.setText(id);
        medicoCB.setSelectedItem(medicoLabel);
        pacienteCB.setSelectedItem(pacienteLabel);
        convenioTxt.setText(convenio);

        btnExcluir.setEnabled(true);
    }//GEN-LAST:event_viewConsultasMouseClicked

    private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirActionPerformed
        // TODO add your handling code here:
        int confirm = JOptionPane.showConfirmDialog(rootPane, "DESEJA EXCLUIR O REGISTRO?");
        if (confirm == 0) {
            String msg = consultaDAO.excluir(Integer.parseInt(txtId.getText()));
            if (msg.equals("1")) {
                msg = "EXCLUSÃO EFETUADA COM SUCESSO";
            }
            JOptionPane.showMessageDialog(rootPane, msg);

            this.limpar();
            this.atualizarTabela();
        }
    }//GEN-LAST:event_btnExcluirActionPerformed

