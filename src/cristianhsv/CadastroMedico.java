    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
        // TODO add your handling code here:
        if (txtNome.getText().trim().isEmpty() || txtCpf.getText().trim().isEmpty() || spnEsp.getValue() == null || spnCrm.getValue() == null) {
            JOptionPane.showMessageDialog(rootPane, "PREENCHA TODOS OS CAMPOS");
        } else {
            Medico medico = new Medico();
            medico.setEspecialidade((Integer) spnEsp.getValue());
            medico.setCrm((Integer) spnCrm.getValue());
            medico.setNome(txtNome.getText().trim());
            medico.setCpf(txtCpf.getText().trim());
            
            String msg;
            String id = txtId.getText().trim();
            if (id.isEmpty()) {
                msg = medicoServices.cadastrar(medico);
                if (msg.equals("1")) {
                    msg = "CADASTRO EFETUADO COM SUCESSO";
                }
                JOptionPane.showMessageDialog(rootPane, msg);
            } else {
                medico.setId(Integer.parseInt(id));
                msg = medicoServices.atualizar(medico);
                if (msg.equals("1")) {
                    msg = "ALTERAÃ‡ÃƒO EFETUADA COM SUCESSO";
                }
                JOptionPane.showMessageDialog(rootPane, msg);
            }
            
            this.limpar();
            this.atualizarTabela();
        }
    }//GEN-LAST:event_btnSalvarActionPerformed

    private void viewMedicosMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_viewMedicosMouseClicked
        // TODO add your handling code here:
        btnExcluir.setEnabled(true);
        
        txtId.setText(viewMedicos.getValueAt(viewMedicos.getSelectedRow(), 0).toString());
        txtNome.setText(viewMedicos.getValueAt(viewMedicos.getSelectedRow(), 1).toString());
        spnEsp.setValue(viewMedicos.getValueAt(viewMedicos.getSelectedRow(), 2));
        spnCrm.setValue(viewMedicos.getValueAt(viewMedicos.getSelectedRow(), 3));
        txtCpf.setText(viewMedicos.getValueAt(viewMedicos.getSelectedRow(), 4).toString());
    }//GEN-LAST:event_viewMedicosMouseClicked

    private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirActionPerformed
        // TODO add your handling code here:
        int resposta = JOptionPane.showConfirmDialog(rootPane, "DESEJA EXCLUIR O REGISTRO?");
        if (resposta == 0) {
            int id = Integer.parseInt(txtId.getText());
            String msg = medicoServices.excluir(id);
            if (msg.equals("1")) {
                msg = "EXCLUSÃƒO EFETUADA COM SUCESSO";
            }
            JOptionPane.showMessageDialog(rootPane, msg);
            
            this.limpar();
            this.atualizarTabela();
        }
    }//GEN-LAST:event_btnExcluirActionPerformed

